 private double parseSalary(String txtSalario) {
        try {
            NumberFormat format = NumberFormat.getCurrencyInstance();
            Number number = format.parse(txtSalario);
            return number.doubleValue();
        } catch (Exception e) {
            // Manejar el error al analizar el salario
            return 0.0;
        }
    }
